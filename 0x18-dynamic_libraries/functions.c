#include <stdio.h>
#include <math.h>

// function for adding two numbers
int add(int a, int b)
{
    return (a + b);
}

// function for subtracting numbers
int sub(int a, int b)
{
    return (a - b);
}

// function for multiplying two number
int mul(int a, int b)
{
    return (a * b);
}

// function for dividing two number
int div(int a, int b)
{
	return (a / b);
}

// function for module two number
int mod(int a, int b)
{
	return (a % b);
}
